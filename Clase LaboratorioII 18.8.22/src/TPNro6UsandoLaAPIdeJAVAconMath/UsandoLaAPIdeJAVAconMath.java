package TPNro6UsandoLaAPIdeJAVAconMath;

import static java.lang.Math.*;

public class UsandoLaAPIdeJAVAconMath {
    public static void main(String[] args){
        double n=45;
        double a=50;
        System.out.println("El valor de n es: "+n);
        System.out.println("El valor de a es: "+a);
        System.out.println("El seno de n es: "+Math.sin(n));
        System.out.println("El coseno de n es: "+Math.cos(n));
        System.out.println("La tangente de n es: "+Math.tan(n));
        System.out.println("El arco tangente de n es: "+Math.atan(n));
        System.out.println("El arco tangente de dos parametros entre n y a es: "+Math.atan2(n,a));
        System.out.println("El valor de e^n es: "+Math.exp(n));
        System.out.println("El logaritmo natural de n es: "+Math.log(n));
        System.out.println("El valor aproximado de Pi es: "+ Math.PI);
        System.out.println("El valor aproximado del numero de Euler es: "+Math.E);
    }
}
